            <h2 style="font-size:40px">Inform Someone About Your Plans</h2>
            <p>Sharing your travel or daily plans with a trusted person ensures that someone knows your whereabouts in case of an emergency.</p>
        </div>
      
        <img class="course-img" src="images/safety-tips/safety4.jpeg" alt="Inform Someone About Your Plans">
        <p>One of the simplest but most effective safety practices is letting someone know where you are going and when you expect to return. This ensures that help can reach you quickly if something goes wrong. Here are some tips to follow:</p>
             <br>
        <h2>1. Share Your Location</h2>
        <p>Use your phone to update a trusted contact about your movements.</p>
        <ul>
            <li>Send live location through messaging apps.</li>
            <li>Inform them of the exact destination and time of return.</li>
            <li>Update them if your plans change unexpectedly.</li>
        </ul> <br>

        <h2>2. Tell Someone You Trust</h2>
        <p>Always choose reliable friends or family to share your plans with.</p>
        <ul>
            <li>Let at least one person know about your travel schedule.</li>
            <li>For late-night outings, share your cab details or driver info.</li>
            <li>Keep your emergency contacts updated in your phone.</li>
        </ul> <br>

        <h2>3. Use Technology Smartly</h2>
        <p>Make use of apps and features designed for safety.</p>
        <ul>
            <li>Enable GPS tracking on safety apps when traveling alone.</li>
            <li>Set up automatic location sharing for regular commutes.</li>
            <li>Use SOS alerts in case you sense danger.</li>
        </ul>
          <br>
        <h2>4. Make it a Habit</h2>
        <p>Consistently informing others builds a strong safety routine.</p>
        <ul>
            <li>Check-in with someone daily if you live alone.</li>
            <li>For long trips, share your itinerary in advance.</li>
            <li>Teach this practice to younger family members too.</li>
        </ul>
