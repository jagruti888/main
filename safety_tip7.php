        <img class="course-img" src="images/safety-tips/safety7.jpg" alt="Use Well-Lit and Busy Routes">
        <p>Whenever you need to walk or travel, especially at night, always prefer well-lit and busy areas. Being in visible and populated places reduces the chances of threats and ensures quicker help if needed.</p>
             <br>
        <h2>1. Prefer Main Roads</h2>
        <p>Stick to streets with higher traffic and people around.</p>
        <ul>
            <li>Avoid shortcuts through isolated alleys or fields.</li>
            <li>Main roads may take longer but are usually safer.</li>
            <li>Stay close to shops, cafes, or open businesses when walking.</li>
        </ul> <br>

        <h2>2. Walk in Groups if Possible</h2>
        <p>Being with others can discourage unwanted attention.</p>
        <ul>
            <li>Travel with friends, colleagues, or neighbors if possible.</li>
            <li>If walking alone, stay near other pedestrians.</li>
            <li>Consider joining local women’s travel safety groups.</li>
        </ul> <br>

        <h2>3. Stay Visible</h2>
        <p>Make sure others can see you easily while traveling.</p>
        <ul>
            <li>Avoid dark, hidden lanes or abandoned roads.</li>
            <li>Use streetlights and open public areas whenever possible.</li>
            <li>Wear reflective clothing or carry a small flashlight if it’s too dark.</li>
        </ul>
          <br>
        <h2>4. Plan Your Route</h2>
        <p>Knowing your path helps you avoid last-minute unsafe decisions.</p>
        <ul>
            <li>Check maps or navigation apps before starting your trip.</li>
            <li>Share your location with a trusted contact while traveling.</li>
            <li>Always keep an alternative safe route in mind.</li>
        </ul>
