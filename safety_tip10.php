             <br>
        <h2>1. Use Trusted Transportation</h2>
        <p>Always rely on safe and verified transport options.</p>
        <ul>
            <li>Use official taxi services or ride-hailing apps.</li>
            <li>Check the driver and vehicle details before getting in.</li>
            <li>Avoid hitchhiking or free rides from unknown people.</li>
        </ul> <br>

        <h2>2. Verify Before Boarding</h2>
        <p>If you are using a cab or ride-share, confirm the details first.</p>
        <ul>
            <li>Ensure the car number matches the booking details.</li>
            <li>Ask the driver for your name before entering.</li>
            <li>Avoid unmarked or suspicious vehicles.</li>
        </ul> <br>

        <h2>3. Stay Alert During Travel</h2>
        <p>Even in a booked ride, remain aware of your surroundings.</p>
        <ul>
            <li>Share your ride details with a trusted contact.</li>
            <li>Avoid being too distracted with your phone or music.</li>
            <li>Keep emergency contacts handy.</li>
        </ul>
          <br>
        <h2>4. Trust Your Instincts</h2>
        <p>If anything feels unsafe, act immediately.</p>
        <ul>
            <li>Politely refuse the ride if you feel uncomfortable.</li>
            <li>Exit the vehicle in safe, public areas if needed.</li>
            <li>Call for help if you sense danger.</li>
        </ul>
